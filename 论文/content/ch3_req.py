"""第 3 章 系统需求分析"""


def write(doc, api):
    api["h1"](doc, "3  系统需求分析")

    api["h2"](doc, "3.1  用户角色分析")
    api["para"](
        doc,
        "尽管这是一个偏向教学演示的工具系统，使用人群仍然可以划分出三种典型角色，"
        "他们对系统的诉求并不完全重叠。"
    )

    api["h3"](doc, "3.1.1  学生用户")
    api["para"](
        doc,
        "学生最关心的是「能否在自己的笔记本或手机上打开链接，立即看到调度过程」。"
        "他们希望调整一组任意的进程参数后立刻得到甘特图与统计指标，"
        "也希望可以保存当前正在演练的题目，第二天到机房用另一台设备继续。"
        "因此账号体系必须做到跨设备同步。"
    )

    api["h3"](doc, "3.1.2  授课教师")
    api["para"](
        doc,
        "教师的诉求集中在「可以暂停、可以单步、可以倍速」"
        "这三个最能配合课堂节奏的功能上。"
        "在讲到时间片轮转的核心思想时，教师希望把 q 改大或改小都能实时看到效果；"
        "在讲到多级反馈队列时，希望可以现场调整队列层数与时间片，"
        "向学生演示「为什么时间片要递增」。"
    )

    api["h3"](doc, "3.1.3  自学者与游客")
    api["para"](
        doc,
        "考虑到部分用户只是被同学分享链接过来「随便看看」，"
        "系统在策略上保留了一个有意为之的设计：强制登录。"
        "看似与游客自由访问相悖，但好处是简化了状态分支——"
        "所有功能都基于已登录用户构建，避免「未登录可以做某些事，登录后能做更多事」"
        "这种不对称带来的复杂度。"
        "同时，注册流程被刻意保持得非常轻：用户名加密码即可，邮箱可填可不填，"
        "把摩擦成本压到最低。"
    )

    api["h2"](doc, "3.2  功能性需求")

    api["h3"](doc, "3.2.1  核心算法仿真模块")
    api["para"](
        doc,
        "系统须实现六种调度算法的完整模拟，并以统一接口暴露给上层："
        "FCFS、SJF、SRTF、RR、PSA（含静态与动态优先级、抢占与非抢占两两组合）、"
        "MFQ（含可配置层数、每级时间片、降级与抢占规则）。"
        "每种算法都必须输出三种衍生数据：时间事件序列、甘特段记录、性能统计指标，"
        "供后续可视化与对比模块复用。"
    )

    api["h3"](doc, "3.2.2  数据可视化模块")
    api["para"](
        doc,
        "可视化模块须包含两个组件。"
        "动态甘特图基于 Canvas，横轴为时间，纵轴依次列出每个进程，"
        "每段 CPU 占用以圆角色块表达；CPU 总线行单独画一行汇总；"
        "当前播放时刻须有一根明显的游标线。"
        "调度舞台基于 SVG，划分四个语义区域：「待到达」「就绪队列」「CPU」「阻塞队列」「完成队列」，"
        "每个进程是一个带颜色与名称的圆角矩形，"
        "在播放过程中通过 Framer Motion 在区域之间平滑过渡。"
        "考虑到进程数量上限为 6，舞台必须能在 1 至 6 个进程之间自适应卡片大小，"
        "始终保证不会溢出队列边界。"
    )

    api["h3"](doc, "3.2.3  交互控制模块")
    api["para"](
        doc,
        "用户交互层须提供三类控件。"
        "进程输入面板支持手动逐条添加（设置上限为 6，超限时弹出 Toast 提示）"
        "或一键随机生成 3 至 6 个进程的测试用例。"
        "播放控制面板提供「开始 / 暂停 / 上一步 / 下一步 / 重置」按钮以及"
        "0.5 倍 / 1 倍 / 2 倍 / 4 倍速度切换；其中「单步」的语义是跳到下一个有意义的事件时刻，"
        "而非简单地推进固定时间增量。"
        "进度条支持点击跳转到任意时刻。"
    )

    api["h3"](doc, "3.2.4  性能评估与对比模块")
    api["para"](
        doc,
        "算法运行结束后，系统自动计算每个进程的完成时间、周转时间、带权周转时间与响应时间，"
        "并给出系统层面的平均指标与 CPU 利用率。"
        "对比页提供两种模式：双算法并排（同一组进程数据下分别运行两种算法，"
        "并排显示甘特图与四项关键指标条形对比）"
        "与全算法矩阵（六种算法同时跑、列出指标矩阵并以五维雷达图直观对比）。"
    )

    api["h3"](doc, "3.2.5  MFQ 设计器")
    api["para"](
        doc,
        "MFQ 设计器是本系统在原始大纲基础上新增的扩展模块，目的是把「可调度参数」"
        "从开发者手里释放给学习者本人。"
        "用户可以通过界面控件指定队列层数（1 至 4），并独立设置每级队列的时间片大小。"
        "系统提供「等差递增」「等比递增」「教材常用」三种预设作为快捷起点，"
        "同时实时校验时间片单调性，对违反「高优先级队列时间片更短」原则的配置给出警示。"
        "运行结束后，除了常规指标，还会展示每级队列命中率、短/长作业平均周转、"
        "降级次数、上下文切换次数与五维加权综合评分（0–100），"
        "并自动跑一组默认 [2, 4, 8] 的基线对比，方便对照改进幅度。"
    )

    api["h3"](doc, "3.2.6  账号与跨设备同步")
    api["para"](
        doc,
        "用户须能够注册个人账号、登录、退出。"
        "登录后的用户可在「我的用例」面板里把当前进程列表与算法配置以命名方式保存，"
        "也可一键加载历史用例。"
        "账号须支持在多台设备登录后看到一致的个人用例。"
    )

    api["h2"](doc, "3.3  非功能性需求")
    api["para"](
        doc,
        "性能层面，单次完整算法仿真应在 50 ms 内完成；"
        "动画帧率维持在 60 FPS；切换算法或更新进程后，整体重新计算与重绘应在 100 ms 内完成。"
        "可访问性层面，遵循 WCAG AA 对比度，"
        "支持键盘焦点环、`prefers-reduced-motion` 媒体查询下自动降级动画。"
        "兼容性层面，支持桌面端 Chrome / Edge / Firefox 最新两个大版本，"
        "支持移动端 Safari、Chrome；"
        "断点覆盖 375 px、768 px、1024 px、1440 px 四档典型宽度。"
        "安全层面，密码以 bcrypt 10 轮哈希存储，会话 Token 采用 HTTP-Only Cookie 防止 XSS 窃取，"
        "生产环境强制 Secure 标志确保仅经 HTTPS 传输。"
    )
