"""第 1 章 绪论"""


def write(doc, api):
    api["h1"](doc, "1  绪论")

    api["h2"](doc, "1.1  研究背景")
    api["para"](
        doc,
        "在计算机专业的本科课程体系里，操作系统几乎是绕不开的一门课。其中"
        "进程调度更是被反复出现的题眼——几乎每一届学生都需要在期末试卷上画一张甘特图，"
        "或者在课堂上跟随老师一笔一画推导短作业优先策略下的平均周转时间。"
        "可问题恰恰出在这里：调度过程本质上是一段连续的时间轴上发生的多次状态迁移，"
        "传统讲法不得不把这种「持续动作」拆成一帧一帧的板书，"
        "每一帧都需要老师额外解释「上一秒发生了什么、为什么现在轮到这个进程」。"
        "对于刚接触并发概念的本科生来说，这种"
        "需要在脑中自行补帧的方式既消耗注意力，也容易在中途漏掉关键的抢占瞬间。"
    )
    api["para"](
        doc,
        "近年来，国内多所高校开始尝试用动画工具或自研小程序辅助教学。"
        "然而这些工具往往是孤立的可执行文件，要么必须在 Windows 平台用 VB 或 C# 打开，"
        "要么是仅供本机演示的命令行脚本，缺少跨平台、可对比、可交互的形态。"
        "笔者在课程实验中也亲身体验过这种割裂感：自己写完一个 SJF 的 Python 脚本，"
        "想给同学演示一遍，却得先压缩源码、再让对方装好 Python 环境，"
        "调试半小时只为了让结果跑出来。"
        "在 Web 技术成熟到可以承载复杂图形的今天，把调度可视化做成一个"
        "「打开浏览器即可使用」的在线系统，从工程上讲并没有想象中那么困难，"
        "而且可以一次开发，长期复用。"
    )

    api["h2"](doc, "1.2  国内外研究现状")
    api["para"](
        doc,
        "国外早期的研究多集中在调度算法本身的形式化分析上。Silberschatz 在《操作系统概念》"
        "中给出了大量经典对照例题；Tanenbaum 的《现代操作系统》也通过示意图把多级反馈队列的降级机制讲得相当透彻。"
        "二者都偏理论描述，并没有提供配套的可交互工具。"
        "Three Easy Pieces 系列把多个调度策略整理成可在终端模拟的 C 程序，思路清晰，"
        "但对没有编程基础的初学者并不友好。"
    )
    api["para"](
        doc,
        "国内的相关探索大致沿着两条路径推进。"
        "一条路径侧重数据可视化与教学方法本身：王垒等学者在《认知负荷理论》框架下"
        "讨论了「将抽象概念转化为图形可显著降低外在认知负荷」的实证证据，"
        "为调度可视化提供了教育学层面的依据；"
        "李远等人则在 Vue.js 与 SVG 组合的递归过程可视化中验证了"
        "动态视图相比静态板书的明显优势。"
        "另一条路径偏向具体实现：王伟在 Vue.js 框架的工程化实践方面给出了较系统的总结；"
        "罗鑫等围绕 SVG 在场站可视化中的稳定性做了大量实证。"
        "可惜的是，这些尝试要么局限于单一算法，要么停留在前端动画层面，"
        "尚未与后端账号体系、数据持久化等工程要素打通，"
        "也几乎没有提供让使用者「自由调参后量化对比」的能力。"
    )
    api["para"](
        doc,
        "综合来看，目前的开源生态里专门面向「操作系统进程调度」、"
        "且同时具备六种主流算法、跨设备账号、动态可视化与设计自由度的 Web 系统仍然空缺。"
        "本研究希望填补的正是这部分空白。"
    )

    api["h2"](doc, "1.3  研究目标与主要内容")

    api["h3"](doc, "1.3.1  研究目标")
    api["para"](
        doc,
        "项目设定了三层目标。第一层是教学使能：用动态甘特图与 SVG 调度舞台代替静态示意图，"
        "让学生在播放、暂停、单步之间自行掌控节奏，把「老师讲到哪儿就停在哪儿」"
        "的体验交还给学习者本身。"
        "第二层是探索平台：除标准的六算法演示之外，提供 MFQ 设计器与算法矩阵对比，"
        "让学习者能够亲手修改时间片或队列层数，立刻看到性能指标的改变。"
        "第三层是可分享性：通过账号体系把个人构造的进程数据持久化到云端，"
        "课堂演示与课后复习共用一份用例，跨设备无缝衔接。"
    )

    api["h3"](doc, "1.3.2  主要内容")
    api["para"](
        doc,
        "围绕上述目标，论文工作大致拆为五块。"
        "第一块是事件驱动的仿真引擎，包含先来先服务（FCFS）、短作业优先（SJF）、最短剩余时间优先（SRTF）、"
        "时间片轮转（RR）、优先级调度（PSA，含静态与动态老化）以及多级反馈队列（MFQ）六种算法的统一实现。"
        "第二块是可视化层，使用 HTML5 Canvas 完成实时甘特图、SVG 配合 Framer Motion 完成调度舞台的过渡动画。"
        "第三块是交互控制，实现播放、暂停、单步、重置以及 0.5/1/2/4 倍速调节，"
        "并支持手动添加进程（上限 6）或一键随机生成测试用例。"
        "第四块是性能评估，自动统计完成时间、周转时间、带权周转、CPU 利用率与上下文切换次数；"
        "在 MFQ 设计器中进一步引入五维加权综合评分。"
        "第五块是账号与持久化，使用 Prisma 与 SQLite 维护用户与个人用例两张表，"
        "通过自定义 JWT 与 HTTP-Only Cookie 在多设备间保持登录状态。"
    )

    api["h2"](doc, "1.4  论文结构")
    api["para"](
        doc,
        "全文共分七章。"
        "第二章梳理调度算法的形式化定义与项目所用的关键技术栈，把背景理论与工程选型放在一起讨论；"
        "第三章给出系统的功能性与非功能性需求，重点说明用户角色与数据范围；"
        "第四章是设计章节，覆盖总体架构、事件驱动契约、数据模型、界面流程与设计令牌；"
        "第五章是实现章节，重点贴出关键算法的 TypeScript 片段与可视化的关键实现思路；"
        "第六章给出测试方法与几组真实运行结果；"
        "第七章总结全文并展望后续可扩展方向。"
    )
