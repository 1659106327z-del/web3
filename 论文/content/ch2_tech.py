"""第 2 章 相关技术与理论基础"""


def write(doc, api):
    api["h1"](doc, "2  相关技术与理论基础")

    api["h2"](doc, "2.1  操作系统进程调度的算法基础")
    api["para"](
        doc,
        "进程调度的核心问题，可以用一句话概括为「在任意时刻决定哪个进程占用 CPU」。"
        "围绕这个问题衍生出来的策略很多，本系统覆盖了下面六种最常被教材选作示范的算法。"
    )

    api["h3"](doc, "2.1.1  先来先服务（FCFS）")
    api["para"](
        doc,
        "FCFS 是最朴素的非抢占式调度策略：进程按照到达时间的先后排成一队，CPU 取出队首执行，"
        "直到主动让出或运行结束。它的实现成本极低，调度决策只需 O(1) 时间。"
        "代价是平均周转时间通常并不出色——一旦先到的是长作业，后续短作业会被生生拖在队列里，"
        "教材里把这个现象戏称为「护航效应」。"
    )

    api["h3"](doc, "2.1.2  短作业优先（SJF）与最短剩余时间优先（SRTF）")
    api["para"](
        doc,
        "SJF 在 CPU 空闲时挑选估计服务时间最短的进程执行，可以从理论上证明它给出"
        "非抢占式调度中最优的平均等待时间。"
        "SRTF 是 SJF 的抢占式推广：每次有新进程到达时重新比较剩余时间，更短者立即抢占。"
        "两个算法的副作用都是长作业可能被无限期推迟，"
        "实际系统通常会引入老化机制来缓解这种饥饿。"
    )

    api["h3"](doc, "2.1.3  时间片轮转（RR）")
    api["para"](
        doc,
        "RR 把 CPU 切成固定长度的「时间片」，就绪队列按 FIFO 组织，"
        "每个被调度的进程最多运行一个时间片，到期后回到队尾。"
        "时间片的取值是 RR 唯一的关键超参：太小则上下文切换开销吃掉收益，"
        "太大则 RR 退化为 FCFS。"
        "MIT 早期的 CTSS 系统正是 RR 思想的发源地，今天的 Linux CFS 调度器虽然换了实现细节，但仍可以看到时间片划分的影子。"
    )

    api["h3"](doc, "2.1.4  优先级调度（PSA）")
    api["para"](
        doc,
        "PSA 给每个进程标注一个优先级数字，值越小代表优先级越高。"
        "本系统支持静态优先级（创建时确定后不再变化）与动态优先级（根据等待时长老化）两种模式，"
        "也允许在抢占式与非抢占式之间切换。"
        "动态优先级的老化策略可以有效缓解低优先级进程被长期挤压的饥饿现象，"
        "这在实时操作系统与混合负载服务器中尤为重要。"
    )

    api["h3"](doc, "2.1.5  多级反馈队列（MFQ）")
    api["para"](
        doc,
        "MFQ 把就绪队列拆成多级，每级拥有自己的时间片。"
        "新到进程一律进入最高优先级队列；用满时间片仍未完成则降级到下一队列；"
        "高优先级队列只要非空，就立即抢占低级队列的执行。"
        "这种结构在不需要事先知道作业长度的情况下，能近似复刻 SJF 对短作业的友好程度，"
        "是 Solaris、早期 UNIX、Windows NT 等主流系统调度器的雏形。"
        "本系统在传统 MFQ 之上做了关键扩展：允许使用者自由调整队列层数（1 至 4）、"
        "为每级配置独立的时间片，并通过五维评分量化反馈不同设计的好坏。"
    )

    api["h2"](doc, "2.2  Web 全栈技术选型")

    api["h3"](doc, "2.2.1  Next.js 14 与 App Router")
    api["para"](
        doc,
        "Next.js 是 Vercel 团队维护的 React 应用框架，14 版本引入的 App Router 提供了更接近"
        "服务器组件思想的目录式路由。"
        "选择 Next.js 而不是更轻量的 Vite + React 主要有三点考虑："
        "其一，Next.js 自带 API Routes，避免再额外搭一套 Express；"
        "其二，App Router 支持 Server Components，可以把账户验证等敏感逻辑保留在服务端；"
        "其三，Next.js 的 Edge Middleware 让强制登录这类全局拦截可以在请求落入页面之前就完成。"
    )

    api["h3"](doc, "2.2.2  TypeScript 与 Tailwind CSS")
    api["para"](
        doc,
        "TypeScript 几乎是当前 React 工程的事实标配。"
        "对于本项目这种涉及大量算法逻辑、事件序列与渲染派生的应用，类型系统提供的"
        "「在编辑器里就能发现问题」的能力非常宝贵。"
        "Tailwind CSS 则把样式写法从分散的 CSS 文件转回到组件附近，"
        "结合自定义的颜色与阴影令牌，可以快速实现一套一致的玻璃拟态视觉。"
    )

    api["h2"](doc, "2.3  前端可视化技术")

    api["h3"](doc, "2.3.1  Canvas 与 SVG 的取舍")
    api["para"](
        doc,
        "在现代浏览器里，二维图形渲染主要有两条路径："
        "Canvas 是命令式的位图，画完即丢，重绘成本随画面像素数线性增长；"
        "SVG 是声明式的 DOM 树，每个图元都是一个独立可绑事件、可由 CSS 控制样式的元素。"
        "本系统的甘特图选择 Canvas，原因在于甘特图的色块数量在播放过程中频繁变化，"
        "用 Canvas 重绘比反复操作 DOM 更轻；"
        "调度舞台选择 SVG，则是因为每个进程方块需要平滑地从一个区域过渡到另一个区域，"
        "SVG 元素天然适合配合 React 的虚拟 DOM 与 Framer Motion 的补间动画。"
    )

    api["h3"](doc, "2.3.2  Framer Motion 的补间机制")
    api["para"](
        doc,
        "Framer Motion 是 React 生态中最流行的动画库之一。"
        "它把 React 组件的位置、尺寸、透明度等属性当成可以被「补间」的状态："
        "当属性发生变化时，库会自动以 spring 物理模型或 tween 缓动函数计算中间帧。"
        "在本系统中，进程方块从就绪队列移动到 CPU 区域并不需要手写任何 keyframe，"
        "只要在每次重新渲染时给它一个新的目标坐标，过渡动画就由 Framer Motion 接管完成。"
    )

    api["h2"](doc, "2.4  状态管理：Zustand 与 MVVM 思想的取舍")
    api["para"](
        doc,
        "开题报告最初设想使用 Vue.js + Element Plus 的 MVVM 体系。"
        "MVVM 的核心是「视图 = 状态的纯函数」，组件不再手动操作 DOM，而是通过响应式数据自动更新。"
        "但在 React 生态里，并不需要把 ViewModel 显式建模成类，函数式 Hooks 就能表达同样的语义。"
        "Zustand 把状态存放在一个外部 store 里，组件按需订阅切片，避免了 React Context 频繁触发"
        "整棵子树重渲染的问题。"
        "对于本系统这种「单源真相 + 多视图同步」的场景，Zustand 在心智模型与性能上都比"
        "传统 Vuex/Pinia 更轻。"
    )

    api["h2"](doc, "2.5  持久化与认证")

    api["h3"](doc, "2.5.1  Prisma 与 SQLite")
    api["para"](
        doc,
        "Prisma 是一套类型安全的 ORM，schema 文件驱动客户端代码生成，开发者写出的查询天然带有类型检查。"
        "SQLite 把数据库以单个文件形式落盘，零运维成本，对单机部署的毕设项目非常合适。"
        "项目把账号信息与个人保存的进程用例放在两张表里，相关的字段含义会在第 4 章详细展开。"
    )

    api["h3"](doc, "2.5.2  自实现 JWT 与 HTTP-Only Cookie")
    api["para"](
        doc,
        "本项目没有引入 NextAuth 等成熟方案，而是用 jose 库手写 HS256 签发与校验、"
        "用 bcryptjs 完成密码哈希，把会话以 HTTP-Only Cookie 的形式保存。"
        "这种「最小可用」的实现既透明、易于在论文中讲清楚每一步，"
        "也避免了生产环境与教学演示混用大型框架带来的额外配置复杂度。"
        "中间件层的会话校验运行在 Edge Runtime，因此专门拆出一份不依赖 bcryptjs 的轻量"
        "Edge 友好型工具模块，避免 Node 原生 API 在 Edge 不可用导致部署失败。"
    )
