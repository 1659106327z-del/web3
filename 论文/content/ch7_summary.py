"""第 7 章 总结与展望"""


def write(doc, api):
    api["h1"](doc, "7  总结与展望")

    api["h2"](doc, "7.1  研究总结")
    api["para"](
        doc,
        "回头看整个项目，最让人有成就感的并不是某一个具体功能，"
        "而是把「事件驱动仿真 + 派生式渲染 + 持久化账号」三件事粘合到同一份代码库里。"
        "调度算法部分覆盖了课本里出现频率最高的六种策略，统一暴露 `events / segments / stats` 三件套，"
        "让甘特图、SVG 舞台、统计表格、矩阵对比都能消费同一份事实数据，"
        "彻底避免了「画面上画的」和「指标里写的」对不上号的尴尬。"
        "可视化层面，Canvas 与 SVG 各司其职——前者跑高频重绘的甘特图，"
        "后者承担更具语义的多区舞台；二者都做到了在 1 至 6 个进程之间自适应排版，"
        "不再出现进程溢出队列的视觉问题。"
        "持久化层面，Prisma + SQLite 加上自实现的 JWT 鉴权，"
        "用极少的代码量提供了「在任何设备登录同一账号都能看到自己的用例」的能力。"
        "MFQ 设计器与算法矩阵则把「调参 → 立即看到结果」的反馈闭环交还给学习者，"
        "也让本系统在国内外现有工具里有了一定的差异化定位。"
    )
    api["para"](
        doc,
        "工程实现上，强制登录策略大大简化了状态分支，"
        "Edge Runtime 中间件与 Node API 路由分离的设计也避免了 bcryptjs 这类原生依赖在边缘运行时的兼容问题。"
        "项目最终通过 Railway 完成了容器化部署，从 GitHub 推送代码到拿到一个永久 HTTPS 网址，"
        "整个 CI/CD 流程几乎是零配置；学校老师只要打开链接就可以使用，无需任何本地准备。"
    )

    api["h2"](doc, "7.2  存在的不足")
    api["para"](
        doc,
        "诚实地讲，本系统仍有几处明显的局限。"
        "其一，SQLite 的并发写性能有限，单机部署足以支撑一个班级同时使用，"
        "但若放到全校公开访问，写竞争与文件锁会成为瓶颈。"
        "其二，进程模型尚未引入真正意义上的 I/O 突发，"
        "目前阻塞队列的语义被简化为「抢占后短暂停留」，"
        "这与真实操作系统里 I/O 等待的概念存在一定距离，"
        "限制了 PSA 与 MFQ 中部分场景的演示力度。"
        "其三，移动端虽然修复了导航问题，但调度舞台与甘特图的字体在 375 px 屏幕上仍偏小，"
        "需要使用者主动放大；后续会通过额外的字体阶梯进一步优化。"
        "其四，目前的算法对比页只支持「同一组进程数据 × 多算法」一种维度，"
        "尚不支持「同一算法 × 多组进程数据」这种横向扩展。"
    )

    api["h2"](doc, "7.3  改进方向")
    api["para"](
        doc,
        "未来的改进可以分四步走。"
        "第一步是引入 I/O 突发模型：在进程定义中增加 `ioBursts` 字段，"
        "调度器在执行时遇到 I/O 时刻把进程移入真正意义上的阻塞队列，"
        "经过指定的 I/O 时间后再放回就绪队列。"
        "这一改动会让本系统的阻塞队列从「装饰」变为有实际语义的概念，"
        "也会显著扩展 MFQ 与 PSA 的演示空间。"
        "第二步是支持把当前用例 + 算法 + 配置打包成可分享的链接或 JSON，"
        "方便老师布置课堂作业、学生互相点评。"
        "第三步是把数据库后端切换到 PostgreSQL，配合 Prisma 的 schema 抽象，"
        "几乎不需要改动业务代码即可支撑多人并发；同时引入「班级排行榜」"
        "让 MFQ 设计器的评分系统在课堂上更具趣味性。"
        "第四步是把成熟的算法实现抽出为独立的 npm 包，"
        "供其他开发者在自己的 Web 教学项目中复用，"
        "也算是对开源社区的一点回馈。"
    )
